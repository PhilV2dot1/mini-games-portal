export function BottomNav() {
  const pathname = usePathname();
  const { t } = useLanguage();

  // Hide on game pages (they need full screen real estate)
  if (pathname.startsWith('/games/') || pathname.startsWith('/blackjack') ||
      pathname.startsWith('/rps') || pathname.startsWith('/tictactoe') ||
      pathname.startsWith('/jackpot') || pathname.startsWith('/2048') ||
      pathname.startsWith('/mastermind') || pathname.startsWith('/spectate')) {
    return null;
  }

  return (
    <nav
      className="md:hidden fixed bottom-0 left-0 right-0 z-40 bg-white/95 dark:bg-gray-900/95 backdrop-blur-md border-t border-gray-200 dark:border-gray-700 shadow-[0_-4px_12px_rgba(0,0,0,0.08)]"
      style={{ paddingBottom: 'env(safe-area-inset-bottom)' }}
    >
      <div className="flex items-stretch justify-around">
        {NAV_ITEMS.map((item) => {
          const isActive = item.href === '/'
            ? pathname === '/'
            : pathname.startsWith(item.href);

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center gap-0.5 flex-1 py-2.5 min-h-[56px] transition-colors ${
                isActive
                  ? 'text-violet-600 dark:text-violet-400'
                  : 'text-gray-500 dark:text-gray-400 hover:text-gray-700 dark:hover:text-gray-300'
              }`}
            >
              {item.icon(isActive)}
              <span className={`text-[10px] font-semibold leading-tight ${isActive ? 'text-violet-600 dark:text-violet-400' : ''}`}>
                {t(item.labelKey) || item.labelKey}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
